def create_or_update_faq(faq_list):
    """
    –°–æ–∑–¥–∞—ë—Ç –Ω–æ–≤—ã–µ –∑–∞–ø–∏—Å–∏ FAQ –∏–ª–∏ –æ–±–Ω–æ–≤–ª—è–µ—Ç —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –ø–æ –≤–æ–ø—Ä–æ—Å–∞–º.
    """
    for item in faq_list:
        question = item["question"]
        answer = item["answer"]
        obj, created = FAQ.objects.get_or_create(
            question=question,
            defaults={"answer": answer}
        )
        if not created:
            obj.answer = answer
            obj.save()
            logging.info(f"–û–±–Ω–æ–≤–ª—ë–Ω —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π FAQ: {question}")
        else:
            logging.info(f"–°–æ–∑–¥–∞–Ω –Ω–æ–≤—ã–π FAQ: {question}")

